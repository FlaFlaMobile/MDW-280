package CalculatriceSimple;

public class MathOutil {

	public int addition (int a , int b) {
		return a+b;
		
	}
	
	public double Cercle (double r) {
		return Math.PI * r * r;
		
	}
	
	public int MaDivision (int a , int b) {
		return a / b;
		
	}
	
	public int MaMultiplication (int a , int b) {
		return a * b;
		
	}
}
